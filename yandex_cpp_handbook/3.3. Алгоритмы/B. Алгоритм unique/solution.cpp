#include <iostream>
#include <vector>

// Объяснение:
// Проверка пустого диапазона: Если диапазон пустой (first == last), функция сразу возвращает last.
// Инициализация:
// result указывает на текущий конец результирующего диапазона без дубликатов. Изначально это первый элемент.
// next указывает на следующий элемент для сравнения. Изначально это второй элемент.
// Итерация: Цикл while проходит по оставшимся элементам диапазона (next).
// Сравнение: Если текущий элемент (*next) отличается от последнего элемента результирующего диапазона (*result), это означает, что мы нашли новый уникальный элемент.
// Перемещение: В этом случае мы:
// Продвигаем result на одну позицию вперед.
// Копируем *next в позицию *result.
// Продвижение: В любом случае, next продвигается к следующему элементу.
// Возврат: После завершения цикла result указывает на последний элемент результирующего диапазона. Мы продвигаем его на одну позицию и возвращаем, чтобы указать на конец диапазона без дубликатов.

template <typename Iter>
Iter Unique(Iter first, Iter last) {
    if (first == last) {
        return last;
    }

    Iter result = first;
    Iter next = first;
    ++next;

    while (next != last) {
        if (*result != *next) {
            ++result;
            *result = *next;
        }
        ++next;
    }
    ++result;
    return result;
}